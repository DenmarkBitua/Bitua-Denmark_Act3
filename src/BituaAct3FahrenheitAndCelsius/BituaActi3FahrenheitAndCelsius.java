package BituaAct3FahrenheitAndCelsius;

public class BituaActi3FahrenheitAndCelsius {
    
    public static void main(String[] args) {
       double Celsius = 76;
       double Fahrenheit = (Celsius * 10/5) + 32;
        
       System.out.println("Temperature in Fahrenheit is: " + Fahrenheit + "F");
        
    }
    
}
